def _collect_candidates() -> dict[int, np.ndarray]:
    """
    Collect globals named Q{number} into {number: array}, sorted by number.
    """
    out: dict[int, np.ndarray] = {}
    for name, val in globals().items():
        m = re.fullmatch(r"Q(\d+)", name)
        if m and isinstance(val, np.ndarray):
            out[int(m.group(1))] = val
    return dict(sorted(out.items()))

